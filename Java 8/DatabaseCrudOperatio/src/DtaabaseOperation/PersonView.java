    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        
        if (txtId.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter Your Id");
        }else if(txtName.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter Your Name");
        }else if(!checkEmail(txtEmail.getText()) || txtEmail.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter Your Email");
        }else{ 
            int id = Integer.parseInt(txtId.getText());          
            String name = txtName.getText();
            String email = txtEmail.getText();
            
            Person p = new Person();
            p.setId(id);
            p.setName(name);
            p.setEmail(email);
            
            PersonUtils.insert(p); 
            displayAllPerson();
            
            txtName.setText("");
            txtEmail.setText("");
            
            lblMsg.setText("Siccessfully Inserted"); 
        }
    }//GEN-LAST:event_btnInsertActionPerformed

