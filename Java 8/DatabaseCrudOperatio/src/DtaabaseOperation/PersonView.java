    public boolean checkEmail(String email){ 
        int a = email.indexOf("@");
        int b = email.lastIndexOf(".");
        if (a > 2 && (b-a) > 2 && b < email.length() - 1) {
            return true;
        }else{ 
            return false;
        }
    }
    public void displayAllPerson(){ 
        List<Person> p = PersonUtils.getAllPerson();
        Person ps = p.get(p.size() - 1);
        txtId.setText(String.valueOf(ps.getId() + 1)); 
        
        DefaultTableModel model =(DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[3];
        model.setRowCount(0); 
        for (int i = 0; i < p.size(); i++) {
            row[0] = p.get(i).getId();
            row[1] = p.get(i).getName();
            row[2] = p.get(i).getEmail();
            model.addRow(row);
            
            
        }
    }
