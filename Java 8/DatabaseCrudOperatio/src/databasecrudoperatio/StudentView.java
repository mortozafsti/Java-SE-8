    public boolean checkEmail(String email){ 
        int atpos = email.indexOf("@");
        int dotpos = email.indexOf(".");
        
        if (atpos > 2 && (dotpos-atpos) > 2 && dotpos < email.length() - 2) {
            return true;
        }else{ 
            return false;
        }
    }
    public void displayAllStudent(){ 
        List<Student> stu = StudentUtils.getAllStudent();
        
        Student std = stu.get(stu.size() - 1);
        txtId.setText(String.valueOf(std.getId() + 1));  
        
        DefaultTableModel model =(DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[3];
        model.setRowCount(0); 
        for (int i = 0; i < stu.size(); i++) {
            row[0] = stu.get(i).getId();
            row[1] = stu.get(i).getName();
            row[2] = stu.get(i).getEmail();
            model.addRow(row); 
            
            
        }
        
    }
