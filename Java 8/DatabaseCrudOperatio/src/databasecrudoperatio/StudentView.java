    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        
        if (txtId.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Enter Your Id");
        }else if(txtName.getText().length() < 1){ 
            JOptionPane.showMessageDialog(null, "Enter Your Name");
        }else if(!checkEmail(txtEmail.getText()) || txtEmail.getText().length() < 1){ 
            JOptionPane.showMessageDialog(null, "Enter Your Email");
        }else{ 
            
            int id = Integer.parseInt(txtId.getText());
            String name = txtName.getText();
            String email = txtEmail.getText();
            
            Student student = new Student();
            student.setId(id);
            student.setName(name);
            student.setEmail(email);
            
            StudentUtils.inser(student);
            displayAllStudent();
            txtName.setText("");
            txtEmail.setText("");
            
            lblMsg.setText("Successfully Inserted"); 
            
        }
    }//GEN-LAST:event_btnInsertActionPerformed

