    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
       
        String  userType= "";
        if (rdAdmin.isSelected() == true) {
            userType = rdAdmin.getText();
        }else{ 
            userType = rdSalesMan.getText();
        }
        User user = new User(txtUsername.getText(), txtPassword.getText(),userType, txtFirstName.getText(), txtLastName.getText(), txtEmail.getText(), txtMobile.getText(), new Date(), true);
        
        UserService.insert(user); 
        lblUMsg.setText("Registration  Success"); 
        
    }//GEN-LAST:event_btnSignUpActionPerformed

